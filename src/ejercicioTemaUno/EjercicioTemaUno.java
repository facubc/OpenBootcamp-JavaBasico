package ejercicioTemaUno;

public class EjercicioTemaUno {

  public static void main(String[] args) {
    int entero = 34;
    long enteroLargo = 214748364;
    double decimal = 3.14;
    char caracter = 'a';
    boolean logico = true;
    String cadena = "cadena de texto";

    System.out.println("int: " + entero);
    System.out.println("long: " + enteroLargo);
    System.out.println("decimal: " + decimal);
    System.out.println("char: " + caracter);
    System.out.println("boolean: " + logico);
    System.out.println("String: " + cadena);

  }

}
